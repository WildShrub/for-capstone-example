import MATH

def add (a,b):
    return a+b

def subtract (a,b):
    return a-b

def multiply (a,b):
    return a*b

def divide (a,b):
    return a/b

def log (a,b):
    return MATH.log(a,b)

def square (a):
    return a^2

def sin (a):
    return MATH.sin(a)

def cos (a):
    return MATH.cos(a)

def square_root (a):
    return MATH.square_root(a)

def percentage (a,b):
    return (a / b) * 100
    